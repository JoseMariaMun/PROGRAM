public class estructuraFOR {
    public static void main(String[] args) {
        //La sentencia FOR se utiliza para recorrer, es decir, repite N veces una acción
        /* al escribir [4 se incluye el nº 4 mientras que al escribir (4 no se incluye.Ej:
        (4.7]--> se incluye el 7 pero no el 4
         */
        for (int i=0;i<5;i++){
            System.out.println("Ejecucion "+i);
        }
    }

}
